"""
Задача: Написать программу, которая генерирует чек для покупателя.

Формат ввода:
- название товара (строка)
- цена товара (натуральное число, рубли за килограмм)
- вес товара (натуральное число, килограммы)
- количество денег у пользователя (натуральное число)

Пример:
Ввод:
манго
187
43
8041

Вывод:
Чек
манго - 43кг - 187руб/кг
Итого: 8041руб
Внесено: 8041руб
Сдача: 0руб
"""

# Читаем данные от пользователя
product_name = input()  # Название товара
price = int(input())    # Цена за кг
weight = int(input())   # Вес в кг
money = int(input())    # Внесенные деньги

# Вычисляем итоговую стоимость и сдачу
total_cost = weight * price  # Общая стоимость
change = money - total_cost  # Сдача

# Печатаем чек
print("Чек")
print(f"{product_name} - {weight}кг - {price}руб/кг")  # Строка с товаром
print(f"Итого: {total_cost}руб")                       # Итоговая стоимость
print(f"Внесено: {money}руб")                          # Внесенные деньги
print(f"Сдача: {change}руб")                           # Сдача